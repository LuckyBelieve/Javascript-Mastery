// object is an object in real life like person or other object with many properties
 
let person = {
name: 'John',
age: 34,
};//object literal

// let log the person on the console
console.log(person);

// Dot notation

// we use dot notation to access the properties of an object either to read or change the object properties 
person.name = "Lucky";

// Bracket notation
person["name"] = "Mary";

console.log(person.name);



